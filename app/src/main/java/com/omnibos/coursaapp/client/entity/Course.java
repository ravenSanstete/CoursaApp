package com.omnibos.coursaapp.client.com.omnibos.coursaapp.client.entity;

public class Course {

	private String moduleId;
	private String courseId;
	private String courseName;
	private String credit;
	private String tutors;
	private String post;
	private String stdNumLimit;
	private String location;
	private String duration;
	private String extraInfo;
	private String examInfo;
	private String classBelong;
	private String restriction;

	public String getModuleId() {
		return moduleId;
	}

	public void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}

	public String getCourseId() {
		return courseId;
	}

	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}

	public String getCourseName() {
		return courseName;
	}

	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}

	public String getCredit() {
		return credit;
	}

	public void setCredit(String credit) {
		this.credit = credit;
	}

	public String getTutors() {
		return tutors;
	}

	public void setTutors(String tutors) {
		this.tutors = tutors;
	}

	public String getPost() {
		return post;
	}

	public void setPost(String post) {
		this.post = post;
	}

	public String getStdNumLimit() {
		return stdNumLimit;
	}

	public void setStdNumLimit(String stdNumLimit) {
		this.stdNumLimit = stdNumLimit;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getDuration() {
		return duration;
	}

	public void setDuration(String duration) {
		this.duration = duration;
	}

	public String getExtraInfo() {
		return extraInfo;
	}

	public void setExtraInfo(String extraInfo) {
		this.extraInfo = extraInfo;
	}

	public String getExamInfo() {
		return examInfo;
	}

	public void setExamInfo(String examInfo) {
		this.examInfo = examInfo;
	}

	public String getClassBelong() {
		return classBelong;
	}

	public void setClassBelong(String classBelong) {
		this.classBelong = classBelong;
	}

	public String getRestriction() {
		return restriction;
	}

	public void setRestriction(String restriction) {
		this.restriction = restriction;
	}

}
